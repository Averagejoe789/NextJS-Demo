// Helper function to convert design system values to CSS
export function getColor(path) {
  const keys = path.split('.');
  let value = designSystem.colors;
  for (const key of keys) {
    value = value[key];
    if (!value) return '#000000';
  }
  return value;
}

export function getSpacing(size) {
  return designSystem.spacing[size] || size;
}

export function getFontSize(size) {
  return designSystem.typography.fontSize[size] || size;
}

export function getShadow(size) {
  return designSystem.shadows[size] || 'none';
}

export function getBorderRadius(size) {
  return designSystem.borderRadius[size] || '0';
}

